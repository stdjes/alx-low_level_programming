#include <stdio.h>

/**
 * main - start point of program
 * this program prints the number count of arguments passed to it.
 *
 * @_argc: holds the arguments counter
 * @_argv: argument value place holder
 *
 * Return: 0 (Success)
 */
int main(int _argc, char **_argv)
{
	printf("%d", _argc);
	return (0);
}
